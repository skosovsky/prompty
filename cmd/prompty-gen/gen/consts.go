package gen

import (
	"fmt"
	"slices"
	"strings"

	"github.com/dave/jennifer/jen"
	"github.com/skosovsky/prompty"
)

// ValidatePromptID returns an error if id is empty, invalid (per prompty.ValidateID), or reserved.
func ValidatePromptID(id string) error {
	if id == "" {
		return fmt.Errorf("manifest id must be non-empty")
	}
	if err := prompty.ValidateID(id); err != nil {
		return err
	}
	if idToConstName(id) == "Prompts" {
		return fmt.Errorf("manifest id %q is reserved (collision with type Prompts)", id)
	}
	return nil
}

// GenerateConstsPackage produces a single file with PromptID type, const block, and AllPromptIDs.
func GenerateConstsPackage(pkgName string, ids []string) (*jen.File, error) {
	f := jen.NewFile(pkgName)
	f.HeaderComment("Code generated by prompty-gen. DO NOT EDIT.")

	sorted := slices.Clone(ids)
	slices.Sort(sorted)

	// type PromptID string
	f.Type().Id("PromptID").String()
	f.Line()

	// const block: PromptIDConst = PromptID("id")
	constDefs := make([]jen.Code, len(sorted))
	for i, id := range sorted {
		constDefs[i] = jen.Id(idToConstName(id)).Id("PromptID").Op("=").Lit(id)
	}
	f.Const().Defs(constDefs...)
	f.Line()

	// func AllPromptIDs() []PromptID
	litVals := make([]jen.Code, len(sorted))
	for i, id := range sorted {
		litVals[i] = jen.Id(idToConstName(id))
	}
	f.Func().Id("AllPromptIDs").Params().Index().Id("PromptID").Block(
		jen.Return(jen.Index().Id("PromptID").Values(litVals...)),
	)

	return f, nil
}

// idToConstName converts manifest id (e.g. "support-agent", "internal/router") to PascalCase const name.
func idToConstName(id string) string {
	s := strings.ReplaceAll(id, "/", "_")
	s = strings.ReplaceAll(s, "-", "_")
	return pascal(s)
}
