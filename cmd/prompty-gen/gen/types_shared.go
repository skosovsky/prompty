package gen

import (
	"slices"

	"github.com/dave/jennifer/jen"
)

// GenerateSharedTypes produces the shared _shared_gen.go file for a package.
// Contains: type PromptID, Prompts struct, NewPrompts, package-level validate, AllPromptIDs.
func GenerateSharedTypes(pkgName string, ids []string) (*jen.File, error) {
	f := jen.NewFile(pkgName)
	f.HeaderComment("Code generated by prompty-gen. DO NOT EDIT.")

	sorted := slices.Clone(ids)
	slices.Sort(sorted)

	f.ImportName("github.com/skosovsky/prompty", "prompty")
	f.ImportName("github.com/go-playground/validator/v10", "validator")

	// type PromptID string
	f.Type().Id("PromptID").String()
	f.Line()

	// var validate = validator.New()
	f.Var().Id("validate").Op("=").Qual("github.com/go-playground/validator/v10", "New").Call()
	f.Line()

	// type Prompts struct { registry prompty.Registry }
	f.Type().Id("Prompts").Struct(
		jen.Id("registry").Qual("github.com/skosovsky/prompty", "Registry"),
	)
	f.Line()

	// func NewPrompts(r prompty.Registry) *Prompts
	f.Func().Id("NewPrompts").Params(
		jen.Id("r").Qual("github.com/skosovsky/prompty", "Registry"),
	).Op("*").Id("Prompts").Block(
		jen.Return(jen.Op("&").Id("Prompts").Values(jen.Dict{
			jen.Id("registry"): jen.Id("r"),
		})),
	)
	f.Line()

	// func AllPromptIDs() []PromptID
	litVals := make([]jen.Code, len(sorted))
	for i, id := range sorted {
		litVals[i] = jen.Id(idToConstName(id))
	}
	f.Func().Id("AllPromptIDs").Params().Index().Id("PromptID").Block(
		jen.Return(jen.Index().Id("PromptID").Values(litVals...)),
	)

	return f, nil
}
