// toPascal converts manifest id (e.g. "support-agent") to PascalCase (e.g. SupportAgent).
func toPascal(s string) string {
	return pascal(strings.ReplaceAll(s, "-", "_"))
}

// isStructSchema returns true when ResponseFormat defines an object schema with non-empty properties.
func isStructSchema(rf *prompty.SchemaDefinition) bool {
	if rf == nil || rf.Schema == nil {
		return false
	}
	typ, _ := rf.Schema["type"].(string)
	if typ != jsonSchemaTypeObject {
		return false
	}
	props, _ := rf.Schema["properties"].(map[string]any)
	return len(props) > 0
}

// GenerateManifestTypes produces a per-manifest _gen.go file.
// Contains: const PromptID, Input/Output types, Render<Name>(ctx, input) (*prompty.PromptExecution, error).
// Output type is generated only when response_format has object schema with properties.
func GenerateManifestTypes(spec *PromptSpec, pkgName string) (*jen.File, error) {
	f := jen.NewFile(pkgName)
	f.HeaderComment("Code generated by prompty-gen. DO NOT EDIT.")

	rootName := toPascal(spec.ID)
	constName := idToConstName(spec.ID)
	inputType := rootName + "Input"

	f.ImportName("context", "context")
	f.ImportName("fmt", "fmt")
	f.ImportName("github.com/skosovsky/prompty", "prompty")

	// const SupportAgent PromptID = "support-agent"
	f.Const().Id(constName).Id("PromptID").Op("=").Lit(spec.ID)
	f.Line()

	// Single schemaMapper for manifest; Input/Output namespaces avoid collisions
	m := newSchemaMapper(rootName)
	hasInputSchema := spec.InputSchema != nil && spec.InputSchema.Schema != nil
	if hasInputSchema {
		if err := validateObjectSchemaForInput(spec.InputSchema.Schema); err != nil {
			return nil, fmt.Errorf("input schema: %w", err)
		}
		stmts, err := m.GenerateTypes(spec.InputSchema.Schema, "Input")
		if err != nil {
			return nil, fmt.Errorf("input schema: %w", err)
		}
		for _, stmt := range stmts {
			f.Add(stmt)
			f.Line()
		}
	} else {
		f.Type().Id(inputType).Struct()
		f.Line()
	}

	hasOutputSchema := isStructSchema(spec.ResponseFormat)
	if hasOutputSchema {
		stmts, err := m.GenerateTypes(spec.ResponseFormat.Schema, "Output")
		if err != nil {
			return nil, fmt.Errorf("response format: %w", err)
		}
		for _, stmt := range stmts {
			f.Add(stmt)
			f.Line()
		}
	}

	// func (p *Prompts) Render<Name>(ctx, input) (*prompty.PromptExecution, error)
	renderName := "Render" + rootName
	varsBlocks, propsCount := buildVarsBlocks(spec)
	body := []jen.Code{
		jen.If(jen.Id("err").Op(":=").Id("validate").Dot("Struct").Call(jen.Op("&").Id("input")).Op(";").Id("err").Op("!=").Nil()).
			Block(
				jen.Return(jen.List(jen.Nil(), jen.Qual("fmt", "Errorf").Call(jen.Lit("validate input: %w"), jen.Id("err")))),
			),
		jen.List(jen.Id("tmpl"), jen.Id("err")).
			Op(":=").
			Id("p").
			Dot("registry").
			Dot("GetTemplate").
			Call(jen.Id("ctx"), jen.Id("string").Call(jen.Id(constName))),
		jen.If(jen.Id("err").Op("!=").Nil()).Block(
			jen.Return(jen.List(jen.Nil(), jen.Qual("fmt", "Errorf").Call(jen.Lit("get template: %w"), jen.Id("err")))),
		),
		jen.Id("vars").Op(":=").Make(jen.Map(jen.String()).Id("any"), jen.Lit(propsCount)),
	}
	body = append(body, varsBlocks...)
	body = append(body,
		jen.List(jen.Id("exec"), jen.Id("err")).Op(":=").Id("tmpl").Dot("Format").Call(jen.Id("vars")),
		jen.If(jen.Id("err").Op("!=").Nil()).Block(
			jen.Return(jen.List(jen.Nil(), jen.Qual("fmt", "Errorf").Call(jen.Lit("format template: %w"), jen.Id("err")))),
		),
		jen.Return(jen.List(jen.Id("exec"), jen.Nil())),
	)

	f.Func().Params(jen.Id("p").Op("*").Id("Prompts")).Id(renderName).Params(
		jen.Id("ctx").Qual("context", "Context"),
		jen.Id("input").Id(inputType),
	).Parens(jen.List(jen.Op("*").Qual("github.com/skosovsky/prompty", "PromptExecution"), jen.Error())).Block(body...)

	return f, nil
}

// buildVarsBlocks returns jen blocks to populate vars map and the initial map capacity.
func buildVarsBlocks(spec *PromptSpec) ([]jen.Code, int) {
	var varsBlocks []jen.Code
	var propsCount int
	if spec.InputSchema == nil || spec.InputSchema.Schema == nil {
		return varsBlocks, 0
	}
	props, _ := spec.InputSchema.Schema["properties"].(map[string]any)
	if props == nil {
		return varsBlocks, 0
	}
	propsCount = len(props)
	required := getRequired(spec.InputSchema.Schema)
	for _, propName := range sortedKeys(props) {
		goName := pascal(propName)
		optional := !required[propName]
		propVal := props[propName]
		propSchema, _ := propVal.(map[string]any)
		typ, _ := propSchema["type"].(string)
		objProps, _ := propSchema["properties"].(map[string]any)
		isPointerOptional := optional &&
			(typ == jsonSchemaTypeString || typ == jsonSchemaTypeInteger || typ == jsonSchemaTypeNumber || typ == jsonSchemaTypeBoolean ||
				(typ == jsonSchemaTypeObject && objProps != nil))
		isRefTypeNoDeref := optional &&
			(typ == jsonSchemaTypeArray || (typ == jsonSchemaTypeObject && objProps == nil) || typ == "")
		isRequiredBool := !optional && typ == jsonSchemaTypeBoolean
		defaultLit, hasDefault := defaultToJenLit(propSchema)
		switch {
		case isRequiredBool:
			// required bool: field is *bool, pass value to vars (not pointer)
			varsBlocks = append(varsBlocks,
				jen.Id("vars").Index(jen.Lit(propName)).Op("=").Op("*").Id("input").Dot(goName),
			)
		case isPointerOptional:
			blk := jen.If(jen.Id("input").Dot(goName).Op("!=").Nil()).Block(
				jen.Id("vars").Index(jen.Lit(propName)).Op("=").Op("*").Id("input").Dot(goName),
			)
			if hasDefault && defaultLit != nil {
				blk = blk.Else().Block(jen.Id("vars").Index(jen.Lit(propName)).Op("=").Add(defaultLit))
			}
			varsBlocks = append(varsBlocks, blk)
		case isRefTypeNoDeref:
			blk := jen.If(jen.Id("input").Dot(goName).Op("!=").Nil()).Block(
				jen.Id("vars").Index(jen.Lit(propName)).Op("=").Id("input").Dot(goName),
			)
			if hasDefault && defaultLit != nil {
				blk = blk.Else().Block(jen.Id("vars").Index(jen.Lit(propName)).Op("=").Add(defaultLit))
			}
			varsBlocks = append(varsBlocks, blk)
		default:
			varsBlocks = append(varsBlocks,
				jen.Id("vars").Index(jen.Lit(propName)).Op("=").Id("input").Dot(goName),
			)
		}
	}
	return varsBlocks, propsCount
}
